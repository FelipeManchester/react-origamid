/* 
useContext

O useContext irá permitir passarmos dados/estados a todos os componentes, sem a necessidade de utilizar propriedades. Serve principalmente para dados/estados GLOBAIS como por exemplo dados do usuário logado.

É uma maneira de compartilhar dados entre componentes em uma ÁRVORE DE COMPONENTES sem precisar passar esses dados manualmente por meio de PROPS em cada nível!

O contexto vem com dois componentes: 
* PROVIDER -> permite que componentes consumidores assinem mudanças no contexto
* CONSUMER -> Utilizado nos componentes que precisam consumir o valor daquele contexto!
*/
